    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarActionPerformed
        Paciente p = new Paciente();
        p.setDni((Long)campoDni.getValue());
        p.setApellido(campoApellido.getText());
        p.setNombre(campoNombre.getText());
        p.setDomicilio(campoDomicilio.getText());
        p.setTelefono(campoTelefono.getText());
        controlador.registrarNuevoPaciente(p);
        limpiarCampos();
    }//GEN-LAST:event_botonAceptarActionPerformed

    private void botonCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCancelarActionPerformed
        limpiarCampos();
    }//GEN-LAST:event_botonCancelarActionPerformed

    private void botonEliminarPacienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarPacienteActionPerformed
        try {
            int[] filas = tablaPacientes.getSelectedRows();
            if(filas.length > 0) {
                PacienteTableModel modelo = (PacienteTableModel)tablaPacientes.getModel();
                for(int i : filas)
                    controlador.eliminarPaciente(modelo.obtenerPaciente(i));
            }
            else
                throw new Exception("No ha seleccionado ningun paciente");
        } catch (NonexistentEntityException ex) {
            JOptionPane.showMessageDialog(null, "Ha ocurrido un error", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botonEliminarPacienteActionPerformed

